export function getSentiment(rating) {
  if (rating >= 4) return "positive";
  if (rating === 3) return "neutral";
  return "negative";
}

export function getSentimentColor(sentiment) {
  switch (sentiment) {
    case "positive": return { bg: "bg-positive-50", text: "text-positive-700", border: "border-positive-200", dot: "bg-positive-500" };
    case "neutral": return { bg: "bg-neutral_tone-50", text: "text-neutral_tone-700", border: "border-neutral_tone-200", dot: "bg-neutral_tone-500" };
    case "negative": return { bg: "bg-negative-50", text: "text-negative-700", border: "border-negative-200", dot: "bg-negative-500" };
    default: return { bg: "bg-surface-50", text: "text-surface-700", border: "border-surface-200", dot: "bg-surface-500" };
  }
}
